     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $request->validate([
            'user' => 'required|min:3',
        ]);

        $theuser = $request->input('user');

        $users = User::search($theuser)->paginate(12);

        return view('search')->with([
                    'users' => $users,
                    'item' => $theuser,
                ]);
    }

